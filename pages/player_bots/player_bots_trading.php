	<script>
		let paragraphs = document.getElementsByTagName("p");
		for(let i = 0; i < paragraphs.length; i++)
        {
			paragraphs[i].addEventListener("click", function()
            {
				let text = this.textContent;
                let prefix = "";
                if(text.includes("corroded")  || text.includes("torn enchanted") || text.includes("tattered silk"))
                {
                    prefix = "multiquest a ";
                }
                else if(text.includes("ancient tarnished") || text.includes("ancient leather") || text.includes("ancient silk") || text.includes("eroded")
                || text.includes("unadorned"))
                {
                    prefix = "multiquest an ";
                }
                else
                {
                    prefix = "buy a ";
                }
				navigator.clipboard.writeText(prefix + text)
					.then(() => console.log(`Copied "${text}" to clipboard!`))
					.catch((error) => console.error(`Failed to copy "${text}" to clipboard: ${error}`));
			});
		}
